function createQcAuditLogs(scenario: ReturnInboundQcChainScenario) {
  const logs = [
    {
      id: `AL-${scenario.qcId}-01`,
      action: 'CREATE_FROM_RETURN_INBOUND',
      detail: `回货入仓批次 ${scenario.batchId} 生成质检记录`,
      at: scenario.inspectedAt,
      by: '系统',
    },
    {
      id: `AL-${scenario.qcId}-02`,
      action: 'SUBMIT_QC',
      detail: `提交质检结果 ${scenario.result}`,
      at: scenario.inspectedAt,
      by: scenario.inspector,
    },
  ]

  if (scenario.liabilityDecidedAt && scenario.liabilityDecidedBy) {
    logs.push({
      id: `AL-${scenario.qcId}-03`,
      action: 'FINAL_LIABILITY_DECISION',
      detail: `责任判定：${scenario.responsiblePartyName ?? scenario.responsiblePartyId ?? '-'}，${scenario.deductionDecision === 'DEDUCT' ? `扣款 ${scenario.deductionAmount ?? 0} CNY` : '不扣款'}`,
      at: scenario.liabilityDecidedAt,
      by: scenario.liabilityDecidedBy,
    })
  }

  if (scenario.writeback?.completedAt && scenario.writeback.completedBy) {
    logs.push({
      id: `AL-${scenario.qcId}-04`,
      action: 'WRITEBACK_SETTLEMENT_CHAIN',
      detail: scenario.settlementImpact.summary,
      at: scenario.writeback.completedAt,
      by: scenario.writeback.completedBy,
    })
  }

  return logs
}

function resolveBatchStatus(scenario: ReturnInboundQcChainScenario): ReturnInboundBatch['qcStatus'] {
  if (scenario.result === 'PASS') {
    return scenario.status === 'CLOSED' ? 'PASS_CLOSED' : 'PASS_CLOSED'
  }

  if (scenario.status === 'CLOSED' && scenario.settlementImpact.status !== 'PENDING_ARBITRATION') {
    return 'QC_CLOSED'
  }

  return 'FAIL_IN_QC'
}

function createInspection(scenario: ReturnInboundQcChainScenario): QualityInspection {
  const inspectedQty = scenario.inspectedQty ?? scenario.returnedQty
  const unqualifiedQty =
    scenario.unqualifiedQty ??
    (scenario.result === 'PASS'
      ? 0
      : Math.min(
          scenario.affectedQty ?? scenario.defectItems?.reduce((sum, item) => sum + item.qty, 0) ?? inspectedQty,
          inspectedQty,
        ))
  const qualifiedQty =
    scenario.qualifiedQty ??
    (scenario.result === 'PASS'
      ? inspectedQty
      : Math.max(inspectedQty - unqualifiedQty, 0))

  return {
    qcId: scenario.qcId,
    refType: 'RETURN_BATCH',
    refId: scenario.batchId,
    refTaskId: scenario.taskId,
    productionOrderId: scenario.productionOrderId,
    inspector: scenario.inspector,
    inspectedAt: scenario.inspectedAt,
    result: scenario.result,
    inspectedQty,
    qualifiedQty,
    unqualifiedQty,
    defectItems: scenario.defectItems ?? [],
    remark: scenario.remark,
    status: scenario.status,
    disposition: scenario.disposition,
    affectedQty: scenario.affectedQty,
    rootCauseType: scenario.rootCauseType,
    responsiblePartyType: scenario.responsiblePartyType,
    responsiblePartyId: scenario.responsiblePartyId,
    responsiblePartyName: scenario.responsiblePartyName,
    liabilityStatus: scenario.liabilityStatus,
    liabilityDecisionStage: scenario.processType === 'SEW' ? 'SEW_RETURN_INBOUND_FINAL' : 'GENERAL',
    liabilityDecisionRequired: scenario.processType === 'SEW',
    deductionDecision: scenario.deductionDecision,
    deductionAmount: scenario.deductionAmount,
    deductionCurrency: scenario.deductionDecision === 'DEDUCT' ? 'CNY' : undefined,
    deductionDecisionRemark: scenario.deductionDecisionRemark,
    liabilityDecidedAt: scenario.liabilityDecidedAt,
    liabilityDecidedBy: scenario.liabilityDecidedBy,
    dispositionRemark: scenario.dispositionRemark,
    disputeRemark: scenario.dispute?.summary,
    arbitrationResult: scenario.basis?.arbitrationResult,
    arbitrationRemark: scenario.basis?.arbitrationRemark,
    arbitratedAt: scenario.dispute?.resolvedAt,
    arbitratedBy: scenario.dispute?.resolvedBy,
    inspectionScene: 'RETURN_INBOUND',
    returnBatchId: scenario.batchId,
    returnProcessType: scenario.processType,
    sourceProcessType: scenario.processType,
    qcPolicy: scenario.qcPolicy,
    returnFactoryId: scenario.returnFactoryId,
    returnFactoryName: scenario.returnFactoryName,
    warehouseId: scenario.warehouseId,
    warehouseName: scenario.warehouseName,
    sourceBusinessType: scenario.sourceBusinessType,
    sourceBusinessId: scenario.sourceBusinessId,
    sourceOrderId: scenario.sourceOrderId,
    sourceReturnId: scenario.batchId,
    sewPostProcessMode: scenario.sewPostProcessMode,
    writebackAvailableQty: scenario.writeback?.availableQty,
    writebackAcceptedAsDefectQty: scenario.writeback?.acceptedAsDefectQty,
    writebackScrapQty: scenario.writeback?.scrapQty,
    writebackCompletedAt: scenario.writeback?.completedAt,
    writebackCompletedBy: scenario.writeback?.completedBy,
    downstreamUnblocked: scenario.writeback?.downstreamUnblocked,
    settlementFreezeReason:
      scenario.settlementImpact.status === 'NO_IMPACT' || scenario.settlementImpact.status === 'READY' || scenario.settlementImpact.status === 'SETTLED'
        ? ''
        : scenario.settlementImpact.summary,
    auditLogs: createQcAuditLogs(scenario),
    createdAt: scenario.inspectedAt,
    updatedAt: scenario.writeback?.completedAt ?? scenario.liabilityDecidedAt ?? scenario.inspectedAt,
    closedAt: scenario.status === 'CLOSED' ? scenario.writeback?.completedAt ?? scenario.liabilityDecidedAt ?? scenario.inspectedAt : undefined,
    closedBy: scenario.status === 'CLOSED' ? scenario.writeback?.completedBy ?? scenario.liabilityDecidedBy ?? scenario.inspector : undefined,
  }
}

function createBatch(scenario: ReturnInboundQcChainScenario): ReturnInboundBatch {
  return {
    batchId: scenario.batchId,
    productionOrderId: scenario.productionOrderId,
    sourceTaskId: scenario.taskId,
    processType: scenario.processType,
    processLabel:
      scenario.processType === 'SEW' && scenario.sewPostProcessMode
        ? SEW_MODE_LABEL[scenario.sewPostProcessMode]
        : PROCESS_LABEL[scenario.processType],
    returnedQty: scenario.returnedQty,
    returnFactoryId: scenario.returnFactoryId,
    returnFactoryName: scenario.returnFactoryName,
    warehouseId: scenario.warehouseId,
    warehouseName: scenario.warehouseName,
    inboundAt: scenario.inboundAt,
    inboundBy: scenario.inboundBy,
    qcPolicy: scenario.qcPolicy,
    qcStatus: resolveBatchStatus(scenario),
    linkedQcId: scenario.qcId,
    sourceType: scenario.sourceBusinessType,
    sourceId: scenario.sourceBusinessId,
    sewPostProcessMode: scenario.sewPostProcessMode,
    createdAt: scenario.inboundAt,
    createdBy: scenario.inboundBy,
    updatedAt: scenario.writeback?.completedAt ?? scenario.liabilityDecidedAt ?? scenario.inspectedAt,
    updatedBy: scenario.writeback?.completedBy ?? scenario.liabilityDecidedBy ?? scenario.inboundBy,
  }
}

function createBasis(scenario: ReturnInboundQcChainScenario): DeductionBasisItem | null {
  if (!scenario.basis) return null

  const liabilityStatusSnapshot =
    scenario.liabilityStatus === 'DRAFT'
      ? 'PENDING'
      : scenario.liabilityStatus === 'VOID'
        ? 'CONFIRMED'
        : scenario.liabilityStatus

  return {
    basisId: scenario.basis.basisId,
    sourceType: scenario.basis.sourceType,
    sourceRefId: scenario.qcId,
    sourceId: scenario.qcId,
    productionOrderId: scenario.productionOrderId,
    taskId: scenario.taskId,
    factoryId: scenario.returnFactoryId,
    settlementPartyType: scenario.responsiblePartyType,
    settlementPartyId: scenario.responsiblePartyId,
    rootCauseType: scenario.rootCauseType,
    reasonCode: 'QUALITY_FAIL',
    qty: scenario.affectedQty ?? 0,
    deductionQty: scenario.basis.deductionQty ?? scenario.affectedQty,
    uom: 'PIECE',
    disposition: scenario.disposition,
    summary: scenario.basis.summary,
    evidenceRefs: scenario.basis.evidenceRefs,
    status: scenario.basis.status,
    deepLinks: {
      qcHref: `/fcs/quality/qc-records/${scenario.qcId}`,
      taskHref: `/fcs/pda/task-receive/${scenario.taskId}`,
    },
    sourceProcessType: scenario.processType,
    sourceReturnId: scenario.batchId,
    sourceBatchId: scenario.batchId,
    sourceBusinessType: scenario.sourceBusinessType,
    sourceBusinessId: scenario.sourceBusinessId,
    qcPolicySnapshot: scenario.qcPolicy,
    decisionStage: scenario.processType === 'SEW' ? 'SEW_RETURN_INBOUND_FINAL' : 'GENERAL',
    responsiblePartyTypeSnapshot: scenario.responsiblePartyType,
    responsiblePartyIdSnapshot: scenario.responsiblePartyId,
    responsiblePartyNameSnapshot: scenario.responsiblePartyName,
    dispositionSnapshot: scenario.disposition,
    deductionDecisionSnapshot: scenario.deductionDecision,
    deductionAmountSnapshot: scenario.basis.deductionAmount,
    settlementReady: scenario.basis.settlementReady,
    settlementFreezeReason: scenario.basis.settlementFreezeReason,
    qcStatusSnapshot: scenario.status,
    liabilityStatusSnapshot,
    deductionAmountEditable: scenario.basis.deductionAmountEditable,
    arbitrationResult: scenario.basis.arbitrationResult,
    arbitrationRemark: scenario.basis.arbitrationRemark,
    arbitratedAt: scenario.dispute?.resolvedAt,
    arbitratedBy: scenario.dispute?.resolvedBy,
    createdAt: scenario.basis.createdAt,
    createdBy: '系统',
    updatedAt: scenario.basis.updatedAt ?? scenario.liabilityDecidedAt ?? scenario.inspectedAt,
    updatedBy: '系统',
    auditLogs: [
      {
        id: `AL-${scenario.basis.basisId}-01`,
        action: 'CREATE_BASIS_FROM_RETURN_INBOUND_QC',
        detail: `由 ${scenario.qcId} 生成扣款依据`,
        at: scenario.basis.createdAt,
        by: '系统',
      },
    ],
  }
}

