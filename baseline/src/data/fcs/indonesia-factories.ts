// 工具函数
export function getFactoryById(id: string): IndonesiaFactory | undefined {
  return indonesiaFactories.find(f => f.id === id)
}

export function getFactoryByCode(code: string): IndonesiaFactory | undefined {
  return indonesiaFactories.find(f => f.code === code)
}

let generatedFactoryCodeSeq = indonesiaFactories.reduce((max, factory) => {
  const matched = factory.code.match(/ID-FAC-(\d+)/)
  if (!matched) return max
  const parsed = Number.parseInt(matched[1], 10)
  return Number.isFinite(parsed) ? Math.max(max, parsed) : max
}, 0) + 1

export function generateFactoryCode(): string {
  const num = String(generatedFactoryCodeSeq).padStart(4, '0')
  generatedFactoryCodeSeq += 1
  return `ID-FAC-${num}`
}

export const FACTORY_POOL_EXCLUDED_TYPES = new Set<FactoryType>([
  'WAREHOUSE',
  'DISPATCH_CENTER',
  'DEV_DESIGN_CENTER',
])

export function isFactoryPoolOrganization(factory: IndonesiaFactory): boolean {
  return !FACTORY_POOL_EXCLUDED_TYPES.has(factory.type)
}

// 检查 tier 是否需要结算
export function needsSettlement(tier: FactoryTier): boolean {
  return tier === 'SATELLITE' || tier === 'THIRD_PARTY'
}

// 检查是否为非生产类型（通常不走加工费结算）
export function isNonProductionType(tier: FactoryTier, type: FactoryType): boolean {
  return tier === 'CENTRAL' && ['WAREHOUSE', 'DISPATCH_CENTER', 'DEV_DESIGN_CENTER'].includes(type)
}

// 验证 tier/type 组合是否有效
export function isValidTierType(tier: FactoryTier, type: FactoryType): boolean {
  return typesByTier[tier].includes(type)
}
